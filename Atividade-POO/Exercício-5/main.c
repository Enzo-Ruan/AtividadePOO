// O que é um Vetor? 

// a) Explique o funcionamento de um vetor e como ele é tratado no C.
// R: Um vetor em C é uma estrutura que armazena elementos do mesmo tipo em posições contíguas de memória, acessados por índices (iniciando em 0).O nome do vetor é um ponteiro para o primeiro elemento. O tamanho é fixo e deve ser definido na compilação.

// b) Explique o que uma string e como ela é tratada em C.
// R: Em C, uma string é um vetor de char finalizado pelo caractere nulo ('\0'). Não há um tipo específico para strings, então elas são manipuladas como arrays de caracteres.